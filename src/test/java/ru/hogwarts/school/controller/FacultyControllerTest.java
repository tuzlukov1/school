    private StudentController studentController;


    @Test
    void getFacultyInfo() throws Exception {
        final long id = 1;
        final String name = "test_name";
        final String color = "test_color";

        JSONObject facultyObject = new JSONObject();
        facultyObject.put("id", id);
        facultyObject.put("name", name);
        facultyObject.put("color", color);

        Faculty faculty = new Faculty();
        faculty.setId(id);
        faculty.setName(name);
        faculty.setColor(color);

        when(facultyRepository.findById(eq(id))).thenReturn(Optional.of(faculty));

        mockMvc.perform(MockMvcRequestBuilders
                        .get("/faculty/" + id)
                        .accept(MediaType.APPLICATION_JSON))
                .andExpect(status().isOk())
                .andExpect(jsonPath("$.id").value(id))
                .andExpect(jsonPath("$.name").value(name))
                .andExpect(jsonPath("$.color").value(color));
    }

    @Test
    void getFacultyByNameOrColor() throws Exception {
        final long id = 1;
        final String name = "test_name";
        final String color = "test_color";

        JSONObject facultyObject = new JSONObject();
        facultyObject.put("id", id);
        facultyObject.put("name", name);
        facultyObject.put("color", color);

        Faculty faculty = new Faculty();
        faculty.setId(id);
        faculty.setName(name);
        faculty.setColor(color);

        List<Faculty> faculties = List.of(faculty);

        when(facultyRepository.findByColorIgnoreCaseOrNameIgnoreCase(eq(name), eq(name))).thenReturn(faculties);

        mockMvc.perform(MockMvcRequestBuilders
                .get("/faculty?nameOrColor=" + name))
                .andExpect(status().isOk())
                .andExpect(jsonPath("$[0].id").value(id))
                .andExpect(jsonPath("$[0].name").value(name))
                .andExpect(jsonPath("$[0].color").value(color));
    }

